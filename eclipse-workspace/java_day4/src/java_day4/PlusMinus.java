package java_day4;

public class PlusMinus {

	public static void main(String[] args) {
		int num = 10;
		
		System.out.println(+num);
		System.out.println(-num);
		System.out.println(num);
		
		num = -num;
		System.out.println(num);

		
		// byte
		byte b = 1;
		System.out.println(b);
		System.out.println(-b);
		
		System.out.println(-b);
		
	}

}
