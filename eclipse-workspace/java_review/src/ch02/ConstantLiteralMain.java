package ch02;

public class ConstantLiteralMain {

	public static void main(String[] args) {
		
		final double PI = 3.14;  // 상수
		
		System.out.println(PI);
		
		// PI 3.141569;
		
		float f = 1.0F;
		char c = 'A';
		int i = 1;
		long l = 1L;
		
		boolean b = true;
		
		// 문자열 : char 배열
	 String s = "Hello";
		
		
		
		
	}

}
